// Document.querySelector() 
    // selects an element by the argument given and be able to manipulate it
    // only able to select the first query 

    document.querySelector('p');
    // selects the first 'p' tag in the document

    document.querySelector('#banner');
    // selects the first element with the ID of banner 

    document.querySelector('.square');
    // selects the first element with class name of square

    document.querySelector('a[title="Java"]');
    //selects the a tag with the specific attribute of 'Java'
    // use [] brackets to target a specific attribute
//
// Document.querySelectorAll()
    // selects all elements with the given selector

    document.querySelectorAll('p');
    // selects all of the p tagged elements

    document.querySelectorAll('p a');
    // selects all the anchor tags within p tags 
